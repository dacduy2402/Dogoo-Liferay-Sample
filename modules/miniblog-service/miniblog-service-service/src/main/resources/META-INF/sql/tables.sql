	companyId LONG,
	userId LONG,
	userName VARCHAR(75) null,
	createDate DATE null,
	modifiedDate DATE null,
	title VARCHAR(75) null,
	image VARCHAR(75) null,
	shortDescription VARCHAR(75) null,
	longDescription VARCHAR(75) null
